<script setup>
import { useRouter, useRoute } from "vue-router";
import { useUserStore } from "../stores/user.js";
import { useCourseStore } from "../stores/course.js";
import { useMaterialsStore } from "../stores/material.js";
import { logout } from "../api/user.js";

const router = useRouter();
const route = useRoute();

const userStore = useUserStore();
const courseStore = useCourseStore();
const materialsStore = useMaterialsStore();

function goBack() {
    router.back();
}

function goMyMaterials() {
    router.push("/mymaterials");
}

async function logoutAndLogin() {
    const response = await logout();
    if (response.ok) {
        materialsStore.$reset();
        userStore.$reset();
        router.push("/login");
    }
}
</script>

<template>
    <nav
        v-if="route.path !== '/login'"
        class="navbar navbar-expand rounded-pill border shadow-sm m-2 fixed-top bg-white"
    >
        <div
            class="container-fluid justify-content-between gap-3 align-items-center"
        >
            <div
                class="d-flex align-items-center justify-content-start gap-2 px-2"
            >
                <div v-show="route.path !== '/courses'">
                    <button class="btn border-0" @click="goBack">
                        <i class="bi bi-arrow-left"></i>
                    </button>
                </div>

                <div
                    class="btn border-0 d-flex flex-row justify-content-between align-items-center p-0 gap-2"
                    @click="router.push('/courses')"
                >
                    <div class="fw-bold fs-4">Mindra</div>
                    <img
                        src="../assets/mindra.png"
                        height="50px"
                        class="rounded-circle"
                        style="aspect-ratio: 1/1"
                    />
                </div>

                <div class="text-secondary">
                    <i class="bi bi-dot"></i>
                </div>

                <div
                    class="d-flex flex-row justify-content-center align-items-center"
                >
                    <button class="btn border-0 p-0 m-0" @click="goMyMaterials">
                        My materials
                    </button>
                </div>
            </div>

            <div class="d-flex justify-content-end align-items-center gap-3">
                <span class="fw-semibold d-none d-sm-block">
                    {{ userStore.name }}
                </span>
                <div class="dropdown">
                    <img
                        :src="userStore.picture"
                        alt="User avatar"
                        class="rounded-circle dropdown-toggle"
                        width="40"
                        height="40"
                        style="object-fit: cover"
                        data-bs-toggle="dropdown"
                    />
                    <ul class="dropdown-menu dropdown-menu-end">
                        <li>
                            <button
                                class="dropdown-item"
                                @click="logoutAndLogin"
                            >
                                Logout
                            </button>
                        </li>
                    </ul>
                </div>
            </div>
        </div>
    </nav>
</template>

